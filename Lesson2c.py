# Set
# defined using curly braces set={}
# not ordered
# does not allow duplicates
# unchangeable but you can add and remove items from a set
password={"admin123",'demo324','demo324','user5462','89064'}
print(password)
# password[0]='user@ryye'
# add
password.add('name1234')
print(password)
# remove
password.remove('admin123')
print(password)
