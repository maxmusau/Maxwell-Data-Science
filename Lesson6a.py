# variable
# container, memory location,name of a value 
# three types
# independent variables, dependent variables,continuous variables

# independent -weight,height,length,width,humidity,temperature-
# not influenced by any external factors but determine an outcome

# dependent variables =the outcome-dependent or influenced by by independent variables e.g weather, BMI ,area,perimeter

# continuous variable-variable with numerical data/value e.g sales,age,height,cost

# categorical variable-variables with non-categorical data e.g  weather, gender,marital status,
